public class Proboscidea {

    // the Bicycle class has three fields
    public String ear;
    public String color;
    public int trunk;

    // the Bicycle class has one constructor
    public Proboscidea(String earSize, String colors, int trunkSize) {
        ear = earSize;
        color = colors;
        trunk = trunkSize;
    }

    // the Bicycle class has four methods
    public void settrunk(int newValue) {
        trunk = newValue;
    }

    public void setcolor(String newValue) {
        color = newValue;
    }

    public void setear(String newValue) {
        ear = newValue;
    }
}
